/*
Comparison operators are:
"==" => equal to
"!=" => not equal to
"<" => less than
"<=" => less than or equal to
">" => greater than
">=" => greater than or equal to
*/
package main

import "fmt"

func main() {
	fmt.Println("cat" == "Cat")
	fmt.Println(10 <= 20)
	fmt.Println(20 != 20.00)
	fmt.Println(!false)

}
